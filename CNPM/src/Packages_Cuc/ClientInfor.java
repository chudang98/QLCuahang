    private void push_data_on_table(ArrayList<Khach_Hang> list) {
        int num_row = model.getRowCount();
        model.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            Khach_Hang kh = list.get(i);

            model.addRow(new Object[]{
                (num_row + 1), kh.getCmnd(), kh.getTen(), kh.getDia_chi(), kh.getSdt(), kh.getEmail(),
                "Select " + (num_row + 1)
            });
        }
        jTable1.getColumnModel().getColumn(6).setCellRenderer(new ButtonRenderer());

        jTable1.getColumnModel().getColumn(6).setCellEditor(new ButtonEditor(new JTextField()));

    }

